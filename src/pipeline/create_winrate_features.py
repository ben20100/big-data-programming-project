# ============================================================
# 1. 라이브러리 import
# ============================================================

import pandas as pd

# ============================================================
# 2. 머신러닝 데이터셋 로드
# ============================================================

df = pd.read_csv("/home/maria_dev/ml_features_v2.csv")

# ============================================================
# 3. 홈 승리 여부 생성
# (홈팀 승리 = 1, 패배 = 0)
# ============================================================

df["home_win"] = (
    df["home_team_goal"]
    >
    df["away_team_goal"]
).astype(int)

# ============================================================
# 4. 원정 승리 여부 생성
# (원정팀 승리 = 1, 패배 = 0)
# ============================================================

df["away_win"] = (
    df["away_team_goal"]
    >
    df["home_team_goal"]
).astype(int)

# ============================================================
# 5. 팀별 홈 승률 계산
# ============================================================

home_win_rate = (
    df.groupby("home_team_api_id")["home_win"]
    .mean()
    .reset_index()
)

# ============================================================
# 6. 홈 승률 컬럼명 변경
# ============================================================

home_win_rate.columns = [
    "home_team_api_id",
    "home_win_rate"
]

# ============================================================
# 7. 팀별 원정 승률 계산
# ============================================================

away_win_rate = (
    df.groupby("away_team_api_id")["away_win"]
    .mean()
    .reset_index()
)

# ============================================================
# 8. 원정 승률 컬럼명 변경
# ============================================================

away_win_rate.columns = [
    "away_team_api_id",
    "away_win_rate"
]

# ============================================================
# 9. 원본 데이터에 홈 승률 조인
# ============================================================

df = df.merge(
    home_win_rate,
    on="home_team_api_id",
    how="left"
)

# ============================================================
# 10. 원본 데이터에 원정 승률 조인
# ============================================================

df = df.merge(
    away_win_rate,
    on="away_team_api_id",
    how="left"
)

# ============================================================
# 11. 홈 승률 - 원정 승률 차이 생성
# ============================================================

df["win_rate_diff"] = (
    df["home_win_rate"]
    -
    df["away_win_rate"]
)

# ============================================================
# 12. 결과 확인
# ============================================================

print(df.shape)

print(
    df[
        [
            "home_team_api_id",
            "away_team_api_id",
            "home_win_rate",
            "away_win_rate",
            "win_rate_diff"
        ]
    ].head()
)

# ============================================================
# 13. 최종 머신러닝 변수 확인
# ============================================================

print(
    df.columns
)
